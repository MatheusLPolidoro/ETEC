package aula7;

public class OperadorSoma {
    public static void main(String[] args) {
        // Programa principal
        int x, y, soma; // declaração das variáveis x e y do tipo inteiro
        x = 20;
        y = 10;
        soma = x + y;
        System.out.println( x + " + " + y + " = " + soma );
    }
}
